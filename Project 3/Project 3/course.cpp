#include "course.h"

//----------------------------------------------------------------------
//							constructor course
//----------------------------------------------------------------------
course::course(){ 
	ID = 0;
    dept = "";
	num = "";
	numHours = 0;
	title = "";
};

//----------------------------------------------------------------------
//	sets for ID, dept, number, hours, and the title of the class
//----------------------------------------------------------------------
void course::set(int newID, string newDept, string newNum, int newHours, string newTitle) {
	ID = newID;
	dept = newDept;
	num = newNum;
	numHours = newHours;
	title = newTitle;
};
//----------------------------------------------------------------------
//							 getID 
//----------------------------------------------------------------------
int course::getID(){
	return ID;
};
//----------------------------------------------------------------------
//							getDept
//----------------------------------------------------------------------
string course::getDept(){
	return dept;
};
//----------------------------------------------------------------------
//							getNum
//----------------------------------------------------------------------
string course::getNum(){ 
	return num;
};
//----------------------------------------------------------------------
//							 getHours
//----------------------------------------------------------------------
int course::getHours(){ 
	return numHours;
};
//----------------------------------------------------------------------
//							getTitle
//----------------------------------------------------------------------
string course::getTitle(){ 
	return title;
};
//----------------------------------------------------------------------
//							print
//----------------------------------------------------------------------
void course::print(){ 
	cout << left << setw(5) << ID << " " << left << setw(4) << dept << " " << left << setw(4) << num << " " << left << setw(2) << numHours << " "
		<< left << setw(50) << title << endl;
};